
-- Table 생성
CREATE TABLE products (
`id` INTEGER UNSIGNED NOT NULL AUTO_INCREMENT,
`name` VARCHAR(255) DEFAULT '',
`price` INTEGER UNSIGNED DEFAULT 0,
`stock` INTEGER UNSIGNED DEFAULT 0,
`created_at` DATETIME DEFAULT CURRENT_TIMESTAMP,
PRIMARY KEY(id)
);

